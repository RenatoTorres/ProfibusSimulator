private void UpdateButton() {
    if( Sim != null && slaveAddr >= 0 )
    {
        if( Sim.isSlaveOnline( slaveAddr ) == true )
            jButtonGoOnline.setText("GO OFFLINE");
        else
            jButtonGoOnline.setText("GO ONLINE");
        }
    }
