private void jButtonGoOnlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGoOnlineActionPerformed
        Sim = ProfibusSimulatorSingleton.getInstance();
        
        if( Sim.isSlaveOnline( slaveAddr ) == true )
            Sim.SetSlaveOnline(slaveAddr, false);
        else
            Sim.SetSlaveOnline(slaveAddr, true);
        
        UpdateButton();
        
        revalidate();
}//GEN-LAST:event_jButtonGoOnlineActionPerformed

